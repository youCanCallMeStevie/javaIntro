public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a*b);
        System.out.println(b/a);
        System.out.println(b%a);
        System.out.println((a*b)+(b));
        System.out.println("The answer is "+(b));





}
}
