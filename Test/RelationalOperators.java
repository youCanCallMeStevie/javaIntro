package src;
public class RelationalOperators {
    public static void main(String[] args) {
        int a = 7;
        int b= 9;

        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);



        
    }
    
}
